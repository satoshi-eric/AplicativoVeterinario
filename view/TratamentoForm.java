    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (formFunction.equals("Cadastrar")) {
            TratamentoDAO.getInstance().create(
                    jTextField1.getText(),
                    jDateChooser1.getCalendar(),
                    jDateChooser2.getCalendar(),
                    this.animal.getId(),
                    jCheckBox1.isSelected()
            );
        } else if (formFunction.equals("Atualizar")) {
            TratamentoDAO.getInstance().update(
                    new Tratamento(
                            tratamento.getId(), 
                            jTextField1.getText(), 
                            jDateChooser1.getCalendar(),
                            jDateChooser2.getCalendar(),
                            this.animal.getId(),
                            jCheckBox1.isSelected()
                    )
            );
        }
        Controller.setTratamentoTableModel(table);
    }//GEN-LAST:event_jButton1ActionPerformed

