    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (formFunction.equals("Cadastrar")) {
            ClienteDAO.getInstance().create(jTextField1.getText(), jTextField2.getText(), jTextField3.getText(), jTextField4.getText(), jTextField5.getText());
        } else if (formFunction.equals("Atualizar")) {
            ClienteDAO.getInstance().update(
                    new Cliente(
                            cliente.getId(), 
                            jTextField1.getText(), 
                            jTextField2.getText(), 
                            jTextField3.getText(), 
                            jTextField4.getText(), 
                            jTextField5.getText()
                    )
            );
        }
        this.setVisible(false);
        Controller.setClienteTableModel(table);
    }//GEN-LAST:event_jButton1ActionPerformed

