    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int selectedRow = jTable1.getSelectedRow();
        Tratamento tratamento = (Tratamento) ((TratamentoTableModel) jTable1.getModel()).getItem(selectedRow);
        Calendar calendar = jDateChooser1.getCalendar();
        int ano = calendar.get(Calendar.YEAR);
        int mes = calendar.get(Calendar.MONTH);
        int dia = calendar.get(Calendar.DAY_OF_MONTH);
        
        if (formFunction.equals("Cadastrar")) {
            ConsultaDAO.getInstance().create(
                new GregorianCalendar(ano, mes, dia),
                Integer.parseInt((String) jComboBox1.getSelectedItem()),
                jTextArea1.getText(),
                animal.getId(),
                veterinario.getId(),
                tratamento == null ? 0 : tratamento.getId(),
                jCheckBox1.isSelected()
            );
        } else if (formFunction.equals("Atualizar")) {
            if (tratamento == null) {
                JOptionPane.showMessageDialog(this, "Selecione um tratamento");
            } else {
                ConsultaDAO.getInstance().update(
                    new Consulta(
                        consulta.getId(),
                        jDateChooser1.getCalendar(),
                        Integer.parseInt((String) jComboBox1.getSelectedItem()),
                        jTextArea1.getText(),
                        consulta.getIdAnimal(),
                        consulta.getIdVeterinario(),
                        consulta.getIdTratamento(),
                        consulta.isTerminou()
                    )
                );
            }
        }
        
        
        Controller.setConsultaTableModel(table);
        this.setVisible(false);
    }//GEN-LAST:event_jButton1ActionPerformed

